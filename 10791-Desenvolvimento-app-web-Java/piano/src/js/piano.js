
function doNota(){
    var doNota = document.getElementById('do');
    var button = document.getElementById('do-button');
    button.style.backgroundColor = "blue";
    button.style.color = "white";
    doNota.onpause;
    doNota.currentTime=0;
    doNota.play();
}

