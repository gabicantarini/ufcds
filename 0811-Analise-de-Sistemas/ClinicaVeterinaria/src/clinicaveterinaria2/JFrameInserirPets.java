   private void PopularComboBox() throws Exception {
       Connection SQLCnn = DriverManager.getConnection("jdbc:mariadb://localhost:3306/?user=root");
       Statement stat1 = SQLCnn.createStatement();
       ResultSet resultadoQuery = stat1.executeQuery("Select NIF,Nome from clinicaveterinaria.clientes values ("+jTextField1+",'"+jTextField2+"','"+jComboBox1+"','"+jTextField4+"')");
       while (resultadoQuery.next()){
           jComboBox1.addItem (resultadoQuery.getString("NIF")+"-"+resultadoQuery.getString("Nome"));
       }
       resultadoQuery.close();

       stat1.close();
       SQLCnn.close();
   }
